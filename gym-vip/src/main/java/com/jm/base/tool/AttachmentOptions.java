package com.jm.base.tool;

/**
 * 附件配置信息
 */
public class AttachmentOptions
{
	/**
	 * 默认状态
	 */
	public static final int NONE = 0x0;

	/**
	 * 显示删除按钮
	 */
	public static final int DELETE = 0x1;

	/**
	 * 显示日期
	 */
	public static final int DATE = 0x2;

	/**
	 * 重新落地附件
	 */
	public static final int RELOAD = 0x4;

	/**
	 * 右键显示下载链接
	 */
	public static final int RIGHTURL = 0x8;

	/**
	 * 历史附件
	 */
	public static final int HISTORY = 0x10;
}
